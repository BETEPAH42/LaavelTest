     */
    public function destroy($id)
    {
        $comments = Comment::where('rescomment_id',$id)->get();
        if($comments->count()) {
            foreach($comments as $comment) {
                Comment::destroy($comment->id);
                // Comment::destroy($id);
            }
            // return redirect()->route('comments.index')->with('error','Ошибка!!! У категории есть записи.');
        }
        Comment::destroy($id);
        return redirect()->route('comments.index')->with('success','Категория успешно удалена.');
    }
